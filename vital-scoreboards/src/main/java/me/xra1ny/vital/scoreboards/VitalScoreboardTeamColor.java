package me.xra1ny.vital.scoreboards;

/**
 * Enumeration of predefined colors for scoreboard teams in the Vital plugin framework.
 * These colors can be used to differentiate and style teams.
 *
 * @author xRa1ny
 */
public enum VitalScoreboardTeamColor {
    /**
     * Represents the color red.
     */
    RED,

    /**
     * Represents the color green.
     */
    GREEN,

    /**
     * Represents the color blue.
     */
    BLUE
}

